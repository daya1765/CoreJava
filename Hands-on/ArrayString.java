package CoreJava;

class ArrayString {

	public static void main(String[] args) {
		//declaring the array for string
		String a[]=new String[3];
		//initializing the array
		a[0]="Raja";
		a[1]="mango";
		a[2]="geeta";
		//traversing the array
		for(int i=0;i<a.length;i++) {
			System.out.println(a[i]);
		}
		
	}

}
